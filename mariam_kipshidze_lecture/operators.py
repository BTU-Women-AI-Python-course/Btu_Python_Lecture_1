# Arithmetic Operators
a = 10
b = 5

print("Arithmetic Operators:")
print("a =", a)
print("b =", b)

# Addition
print("Addition: a + b =", a + b)

# Subtraction
print("Subtraction: a - b =", a - b)

# Multiplication
print("Multiplication: a * b =", a * b)

# Division
print("Division: a / b =", a / b)

# Floor Division
print("Floor Division: a // b =", a // b)

# Modulus
print("Modulus: a % b =", a % b)

# Exponentiation
print("Exponentiation: a ** b =", a ** b)

# Comparison Operators
print("\nComparison Operators:")
print("a =", a)
print("b =", b)

# Equal to
print("Equal to: a == b =", a == b)

# Not equal to
print("Not equal to: a != b =", a != b)

# Greater than
print("Greater than: a > b =", a > b)

# Less than
print("Less than: a < b =", a < b)

# Greater than or equal to
print("Greater than or equal to: a >= b =", a >= b)

# Less than or equal to
print("Less than or equal to: a <= b =", a <= b)

# Logical Operators
print("\nLogical Operators:")
x = True
y = False

# AND
print("AND: x and y =", x and y)

# OR
print("OR: x or y =", x or y)

# NOT
print("NOT: not x =", not x)
print("NOT: not y =", not y)

# Assignment Operators
print("\nAssignment Operators:")
c = 15

# Simple Assignment
print("Simple Assignment: c =", c)

# Addition Assignment
c += 5
print("Addition Assignment: c += 5, c =", c)

# Subtraction Assignment
c -= 3
print("Subtraction Assignment: c -= 3, c =", c)

# Multiplication Assignment
c *= 2
print("Multiplication Assignment: c *= 2, c =", c)

# Division Assignment
c /= 4
print("Division Assignment: c /= 4, c =", c)

# Modulus Assignment
c %= 3
print("Modulus Assignment: c %= 3, c =", c)

# Exponentiation Assignment
c **= 2
print("Exponentiation Assignment: c **= 2, c =", c)

# Bitwise Operators
print("\nBitwise Operators:")
d = 60  # 0011 1100 in binary
e = 13  # 0000 1101 in binary

# Bitwise AND
print("Bitwise AND: d & e =", d & e)

# Bitwise OR
print("Bitwise OR: d | e =", d | e)

# Bitwise XOR
print("Bitwise XOR: d ^ e =", d ^ e)

# Bitwise NOT
print("Bitwise NOT: ~d =", ~d)

# Bitwise Left Shift
print("Bitwise Left Shift: d << 2 =", d << 2)

# Bitwise Right Shift
print("Bitwise Right Shift: d >> 2 =", d >> 2)
